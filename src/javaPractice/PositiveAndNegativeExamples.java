package javaPractice;

public class PositiveAndNegativeExamples {
    public static void main(String[] args) {
        int number = -13;
        if (number>0){
            System.out.println("Positive");
        } else if (number<0){
            System.out.println("Negative");
        } else {
            System.out.println("You've entered zero");
        }
    }
}
